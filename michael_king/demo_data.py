from michael_king.notes import enrich

# (מספר MIDI, משך בשניות). C4=60. None = שתיקה קצרה.
_TWINKLE = [
    (60, 0.5), (60, 0.5), (67, 0.5), (67, 0.5), (69, 0.5), (69, 0.5), (67, 1.0),
    (65, 0.5), (65, 0.5), (64, 0.5), (64, 0.5), (62, 0.5), (62, 0.5), (60, 1.0),
    (67, 0.5), (67, 0.5), (65, 0.5), (65, 0.5), (64, 0.5), (64, 0.5), (62, 1.0),
    (67, 0.5), (67, 0.5), (65, 0.5), (65, 0.5), (64, 0.5), (64, 0.5), (62, 1.0),
    (60, 0.5), (60, 0.5), (67, 0.5), (67, 0.5), (69, 0.5), (69, 0.5), (67, 1.0),
    (65, 0.5), (65, 0.5), (64, 0.5), (64, 0.5), (62, 0.5), (62, 0.5), (60, 1.0),
]


def _build(sequence, gap=0.06):
    notes = []
    t = 0.0
    for midi, dur in sequence:
        if midi is not None:
            notes.append(
                {"midi": midi, "startTime": round(t, 3), "endTime": round(t + dur, 3)}
            )
        t += dur + gap
    return notes


def demo_notes():
    return enrich(_build(_TWINKLE))
